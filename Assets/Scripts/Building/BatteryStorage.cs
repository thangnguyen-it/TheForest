using TheForest.Building.Config;
using TheForest.Building.Core;
using TheForest.Building.Events;
using UnityEngine;

public class BatteryStorage : MonoBehaviour
{
    [SerializeField] private ElectricityConfig config;
    [SerializeField] private float chargeWh;   // current charge

    public float ChargeWh => chargeWh;
    public float CapacityWh => config != null ? config.batteryCapacityWh : 50f;
    public bool HasCharge => chargeWh > 0.001f;

    /// <summary>Add watt-hours (from solar charging).</summary>
    public void Charge(float wh)
    {
        chargeWh = Mathf.Min(CapacityWh, chargeWh + wh);
    }

    /// <summary>Draw watt-hours (to power consumers at night). Returns actual drawn.</summary>
    public float Draw(float wh)
    {
        float drawn = Mathf.Min(chargeWh, wh);
        chargeWh -= drawn;
        if (chargeWh <= 0f)
            EventBus<BatteryDepletedEvent>.Raise(new BatteryDepletedEvent(gameObject));
        return drawn;
    }
}
